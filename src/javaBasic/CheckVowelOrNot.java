package javaBasic;

public class CheckVowelOrNot {

	public static void main(String[] args) {

		// a,e,i,o,u
		// input a = vowel and input j=Not Vowel
		char vowel = 'i';

		if (vowel == 'i' || vowel == 'A' || vowel == 'E' || vowel == 'o' || vowel == 'u') {
			System.out.println("This is vowel");
		} else {
			System.out.println("This is not vowel");
		}

	}

}
